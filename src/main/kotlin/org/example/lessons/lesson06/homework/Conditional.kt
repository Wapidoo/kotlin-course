//Создайте функцию, которая конвертирует температуру из градусов Цельсия в Фаренгейты и наоборот в зависимости
// от указанной единицы измерения (C/F). Единицу измерения нужно передать вторым аргументом функции.
// Несколько аргументов передаются через запятую. Распечатай в консоль результат конвертации
// с добавлением единицы измерения. Чтобы добавить единицу измерения после результата используй функцию печати
// без переноса строки print("C") или print("F").

fun task6(temp: Double, scale: Char) {
    if (scale == 'C') {
        print((temp * 9 / 5)+32)
        println("F")
    }
    else {
        print((temp - 32) / 1.8)
        println("C")
    }
}

//Напишите функцию, которая на основе температуры воздуха рекомендует тип одежды: "куртка и шапка" при температуре
// ниже +10, "ветровка" от +10 до +18 градусов включительно и "футболка и шорты" при температуре выше +18 градусов.
